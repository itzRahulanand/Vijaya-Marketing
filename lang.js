// State variable for current language
let currentLang = 'en';

/**
 * Toggles the language between English and Malayalam
 * and updates all elements with a [data-i18n] attribute.
 */
function toggleLanguage(e) {
    if (e) e.preventDefault();

    // Switch language state
    currentLang = currentLang === 'en' ? 'ml' : 'en';

    // Select all translatable elements
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');

        // Handle specific attributes (like title or placeholder if needed) vs innerHTML
        if (translations[currentLang][key]) {
            // we use innerHTML here to allow <br> tags and <span> elements (like stars) to render correctly
            el.innerHTML = translations[currentLang][key];
        }
    });

    // Update document language attribute for accessibility
    document.documentElement.lang = currentLang;
}

// Attach the event listener to the toggle button once DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.addEventListener('click', toggleLanguage);
    }
});
